public class Calc{
    public long add(int a,int b){
        return a + b;
    }

    public long sub(int a,int b){
        return a - b;
    }

    public long multi(int a,int b){
        return a * b;
    }

    public double divi(int a,int b){
        return a / b;
    }

    public long secret(int a,int b){
        return ((a+b)*(a-b));
    }
}